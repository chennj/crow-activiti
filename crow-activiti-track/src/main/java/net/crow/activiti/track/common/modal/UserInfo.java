package net.crow.activiti.track.common.modal;

public class UserInfo {

	private String id;	
	private String no;
	private String name;
	private String roleTypes;
	private String roleNames;
	private String sys;
	private String belongTypeIds;
	private String belongTypeNames;
	private String authIds;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRoleTypes() {
		return roleTypes;
	}
	public void setRoleTypes(String roleTypes) {
		this.roleTypes = roleTypes;
	}
	public String getRoleNames() {
		return roleNames;
	}
	public void setRoleNames(String roleNames) {
		this.roleNames = roleNames;
	}
	public String getSys() {
		return sys;
	}
	public void setSys(String sys) {
		this.sys = sys;
	}
	public String getBelongTypeIds() {
		return belongTypeIds;
	}
	public void setBelongTypeIds(String belongTypeIds) {
		this.belongTypeIds = belongTypeIds;
	}
	public String getBelongTypeNames() {
		return belongTypeNames;
	}
	public void setBelongTypeNames(String belongTypeNames) {
		this.belongTypeNames = belongTypeNames;
	}
	public String getAuthIds() {
		return authIds;
	}
	public void setAuthIds(String authIds) {
		this.authIds = authIds;
	}
	
	
}
